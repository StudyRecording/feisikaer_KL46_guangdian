#define START_YE 1 //定义显示开始页数
#define START_LIE 47 //定义显示开始列数

void ShowVideoBin1(unsigned char a[HANG][LIE])
{
	uint8_t i = 0,j = 0,h = 0;
	uint8_t temp = 0;
	uint8_t ye = 0,ye_last = 0;
  uint8_t datatemp ;
	
	ye = 48/8; //将二值化数据分页
	ye_last = 48%8;
	if(ye_last!= 0)
	{
		ye++;		
	}
	else
	{
              ye_last = 8;
	}

	
	for(h=0;h < ye-1;h++)
	{	
		LCD_Set_Pos(START_LIE,h+START_YE);//定义开始显示位
		for(j=0;j<LIE;j++)//打印一页数据
		{
			for(i = 0;i < 8;i ++)//竖着打印一个字节的数据
			{
				datatemp = a[i+8][j];
				datatemp <<=i;
				temp |=datatemp;
			}
			
			LCD_WrDat(~temp);
			temp = 0;
		}
		a += 8;
	}
LCD_Set_Pos(START_LIE,h+START_YE);//定义开始显示位
	for(j=0;j<LIE;j++)//打印一页数据
		{
			for(i = 0;i < ye_last;i ++)//竖着打印一个字节的数据
			{
				datatemp = a[i+8][j];
				datatemp <<=i;
				temp |=datatemp;
			}
			
			LCD_WrDat(~temp);
			temp = 0;
		} 
}

